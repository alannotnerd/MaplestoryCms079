var status = -1;
var level = 10
var itemId = 1142345;

function start(mode, type, selection) {
    if (qm.haveItem(itemId, 1)) {
        qm.completeQuest();
    } else if (qm.canHold(itemId, 1) && qm.getLevel() >= level) {
        qm.gainItem(itemId, 1);
        qm.completeQuest();
    }
    qm.dispose();
}

function end(mode, type, selection) {
    if (qm.haveItem(itemId, 1)) {
        qm.completeQuest();
    } else if (qm.canHold(itemId, 1) && qm.getLevel() >= level) {
        qm.gainItem(itemId, 1);
        qm.completeQuest();
    }
    qm.dispose();
}